def get_state_from_gstin(gstin: str) -> str:
    """
    Return state name from GSTIN, or empty string if invalid.
    """
    if not gstin or len(gstin) < 2:
        return ""
    code = gstin[:2]
    return GST_STATE_CODES.get(code, "")
